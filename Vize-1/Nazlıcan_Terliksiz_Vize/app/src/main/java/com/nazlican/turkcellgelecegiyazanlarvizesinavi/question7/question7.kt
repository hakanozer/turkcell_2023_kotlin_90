/*
- Class : Nesneleri oluşturduğumuz yerdir.
Nesnelerin özelliklerini yani değişkenleri ve fonksiyonları içerir.

- Object : Bir nesne, bir sınıfın örneğidir.
Bir sınıftan birden çok nesne oluşturulabilir.
Örneğin "Ev" sınıfından "müstekil" ve "daire" gibi farklı evler oluşturabiliriz.
Her biri sınıf tarafından tanımlanan özelliklere ve davranışlara sahip olur.

- Abstract : Soyut sınıflar, diğer sınıfların temelini oluşturur fakat abstract class lardan nesne oluşturulamaz
Bunun yerine abstract sınıflar alt sınıflar tarafından genişletilerek kullanılır.
Örneğin, "Hayvan" soyut sınıfı, "Köpek" ve "Kedi" gibi alt sınıflar tarafından genişletilebilir.

- Interface : Bir arayüz, bir sınıfın uygulaması gereken metotları ve davranışları tanımlar.
Interface'leri hazır taslak olarak düşünebiliriz, classlara özellik katmak için kullanılır.
Bir classa birden fazla class implement edilebilir. İnheritance'lardan en büyük farklarından biridir.
Interface i yazınca hata verir çünkü içinde override edilmesi gereken metodlar vardır. Onları override etmemiz gerekir.

*/
